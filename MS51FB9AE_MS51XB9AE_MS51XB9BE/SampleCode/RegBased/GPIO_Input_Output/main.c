//***********************************************************************************************************
//  File Function: MS51 GPIO toggle demo code
//***********************************************************************************************************
#include "ms51_16k_sdcc.h"


//----------------------------------------------------------------------------------------------//
void main (void)
{
  ALL_GPIO_QUASI_MODE;
  MODIFY_HIRC(HIRC_24);
  while(1)
  {
    P1 = 0xff;
    Timer3_Delay(16000000,16,400,1000);
    P1=0x00;
    Timer3_Delay(16000000,16,400,1000);
  }
}



